    @Override
    public void beansDropped(final ArrayList<CidsBean> beans) {
        if (cidsBean != null) {
            for (final CidsBean bean : beans) {
                if (bean.getClass().getName().equals("de.cismet.cids.dynamics.Indikator")) {
                    try {
                        cidsBean.setProperty("indikator_schluessel", bean);
                    } catch (Exception e) {
                        log.error(e, e);
                    }
                }
            }
        }
    }
