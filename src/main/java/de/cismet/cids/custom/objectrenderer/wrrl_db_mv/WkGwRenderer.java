    @Override
    public CidsBean getCidsBean() {
        return cidsBean;
    }

    @Override
    public void setCidsBean(final CidsBean cidsBean) {
        bindingGroup.unbind();
        this.cidsBean = cidsBean;

        if (cidsBean != null) {
            wkGwPanOne.setCidsBean(cidsBean);
            wkGwPanTwo.setCidsBean(cidsBean);
            wkGwPanThree.setCidsBean(cidsBean);
            wkGwPanFour.setCidsBean(cidsBean);
            wkGwPanFive.setCidsBean(cidsBean);
            wkGwPanSix.setCidsBean(cidsBean);
            browserPanel.loadUrl("https://fis-wasser-mv.de/charts/steckbriefe/gw/gw_wk.php?gw="
                        + String.valueOf(cidsBean.getProperty("name")));
        }
    }

    @Override
    public void dispose() {
        wkGwPanOne.dispose();
        wkGwPanTwo.dispose();
        wkGwPanThree.dispose();
        wkGwPanFour.dispose();
        wkGwPanFive.dispose();
        wkGwPanSix.dispose();
        bindingGroup.unbind();
    }

    @Override
    public String getTitle() {
        return org.openide.util.NbBundle.getMessage(WkGwRenderer.class, "WkGwRenderer.title")
                    + String.valueOf(cidsBean);
    }

